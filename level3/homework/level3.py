# დავალება 3
# შევქმენი 2 ცვლადი, რომლის მნიშვნელობა იყო integer ანუ მთელი რიცხვები
num1 = 10
num2 = 2
# დავიწყეთ დაბეჭბდა არითმეტიკული ოპერაციების
print(num1 + num2)      # შევკრიბეთ რიცხვები
print(num1 - num2)      # გამოვაკელით რიცხვები
print(num1 * num2)      # გავამრავლეთ რიცხვები
print(num1 / num2)      # გავყაბით რიცხვები




